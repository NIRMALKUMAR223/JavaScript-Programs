// JavaScript Program to Append an Object to an Array

const arr  =[];

const obj1 = {
    name : 'Nirmal',
    age : 22,
}

const obj2 = {
    name : 'Kumar',
    age : 22,
}

const obj3 = {
    name : 'Rock',
    age : 22,
}

// add an object to an array by using inbuild method push()
// it is used to push any element to an array
arr.push(obj1,obj2,obj3);

console.log(arr);
